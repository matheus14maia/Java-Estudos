class Cereal {
    String nome;
    String minerais;
    String gluten;
    Double teorDeFibras;
    Cereal(String nome, String minerais, String gluten, Double teorDeFibras){
        this.nome = nome;
        this.minerais = minerais;
        this.gluten = gluten;
        this.teorDeFibras = teorDeFibras;
    }
}
